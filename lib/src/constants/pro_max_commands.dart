import 'dart:typed_data';

abstract class TreadmillCommands {
  static Uint8List get initialize =>
      Uint8List.fromList([0xf6, 0xa0, 0x80, 0x10, 0x78, 0xf4]);

  static Uint8List get verifyError =>
      Uint8List.fromList([0xf6, 0x1a, 0x8b, 0x3e, 0xf4]);

  static Uint8List get stop0xff =>
      Uint8List.fromList([0xf6, 0xa0, 0xff, 0xf7, 0x00, 0x39, 0xf4]);

  static Uint8List get stop0x00 =>
      Uint8List.fromList([0xf6, 0xa0, 0x00, 0xb0, 0x79, 0xf4]);

  static Uint8List get speed1200hz =>
      Uint8List.fromList([0xf6, 0x90, 0x04, 0xb0, 0x99, 0x72, 0xf4]);

  static Uint8List get speed4000hz =>
      Uint8List.fromList([0xf6, 0x90, 0x0f, 0xa0, 0x65, 0x74, 0xf4]);

  static Uint8List get speed6000hz =>
      Uint8List.fromList([0xf6, 0x90, 0x17, 0x70, 0xf7, 0x09, 0x7f, 0xf4]);

  static Uint8List get inclination1 =>
      Uint8List.fromList([0xf6, 0x98, 0x00, 0x43, 0xde, 0xb1, 0xf4]);

  static Uint8List get inclination4 =>
      Uint8List.fromList([0xf6, 0x98, 0x01, 0x0a, 0xb8, 0x71, 0xf4]);
}
