import 'dart:typed_data';

abstract class TreadmillCommands {
  static Uint8List get initialize =>
      Uint8List.fromList([0xf6, 0xa0, 0x80, 0x10, 0x78, 0xf4]);

  static Uint8List get verifyError =>
      Uint8List.fromList([0xf6, 0x1a, 0x8b, 0x3e, 0xf4]);

  static Uint8List get stop0xff =>
      Uint8List.fromList([0xf6, 0xa0, 0xff, 0xf7, 0x00, 0x39, 0xf4]);

  static Uint8List get stop0x00 =>
      Uint8List.fromList([0xf6, 0xa0, 0x00, 0xb0, 0x79, 0xf4]);
}
